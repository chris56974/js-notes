/** 
 * In JS you can't declare an interface, 
 * but you can implement one by giving an object all the methods that it describes. 
 * The array, string and map objects all implement the Iterable interface 
 * because they have the methods that it describes. 
 * 
 * A protocol is a bunch of interfaces + the rules for using them.
 * The iterable is the thing you can traverse sequentially, and the iterator is the pointer used for the traversal.
 */