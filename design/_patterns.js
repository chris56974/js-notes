/** 
 * They're reusable templates for solving common software design problems.
 * Behavioural design patterns are concerned with communication between objects
 * Structural design patterns are concerned with class and object composition. 
 * They use inheritance to compose interfaces and define ways to compose objects to obtain new functionality.
 * Creational design patterns are concerned with creating objects rather than instantiating them directly. 
 * They typically have more flexibility in deciding which objects need to be created for a given case.
 */